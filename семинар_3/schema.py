class AspectScore(BaseModel):
    """Оценка одного аспекта."""
    aspect: str
    score: Optional[float] = Field(None, ge=1.0, le=5.0)
    evidence: Optional[str] = None
    mentioned: bool

    @field_validator("score")
    @classmethod
    def score_when_mentioned(cls, v: Optional[float], info) -> Optional[float]:
        if info.data.get("mentioned") and v is None:
            raise ValueError("score обязателен если mentioned=True")
        return v


