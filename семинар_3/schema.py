# Раунд 1 — Information Extraction

class Issue(BaseModel):
    """Конкретная проблема или похвала, упомянутая в отзыве."""
    description: str = Field(..., description="Краткое описание (1-2 предложения)")
    quote: str = Field(..., description="Точная цитата из отзыва")
    sentiment: Literal["positive", "negative", "neutral"]
    category: Literal[
        "производительность", "дизайн", "поддержка",
        "цена", "реклама", "надёжность", "другое"
    ]


class Review(BaseModel):
    """Структурированный отзыв."""
    review_id: str
    app_name: str
    rating: int = Field(..., ge=1, le=5)
    review_date: Optional[date] = None
    issues: list[Issue] = Field(default_factory=list)
    overall_sentiment: Literal["positive", "negative", "neutral", "mixed"]
    would_recommend: Optional[bool] = None

    @field_validator("review_date")
    @classmethod
    def date_not_in_future(cls, v: Optional[date]) -> Optional[date]:
        if v is not None and v > date.today():
            raise ValueError(f"Дата отзыва {v} позже сегодняшней")
        return v

    @field_validator("issues")
    @classmethod
    def at_least_one_issue(cls, v: list[Issue]) -> list[Issue]:
        if len(v) == 0:
            raise ValueError("Должен быть хотя бы один issue")
        return v


